    void show_res(question start)
    {
        int score=0;
        while(start!=null)
        {
            if(start.choice.equals(start.ans))
                score++;
            else
            {
                result_label.setText(result_label.getText()+"<html>Q-"+start.q+"<br>Right Answer="+start.ans+"<br>Your Answer="+start.choice+"<br><br>");
                //result_label.repaint();
                //result_label.revalidate();
            }
            //System.out.println(result_label.getText()+"\n"+start.q+"\nRight Answer="+start.ans+"\nYour Answer="+start.choice);
            start=start.nxt;
        }
        
        score_label.setText("<html><b><font color=#00FF6C>"+current_user.username+"'s score: "+Integer.toString(score)+"</font></b></html>");
    }
    
